package ca.ualberta.cs.lonelytwitter;



public class Summary
{
	int numOfTweets;
	long avgLenOfTweets;
	
	public int getNumOfTweets()
	{
		return numOfTweets;
	}
	
	public void setNumOfTweets(int numOfTweets)
	{
	
		this.numOfTweets = numOfTweets;
	}
	
	
	public long getAvgLenOfTweets()
	{
	
		return avgLenOfTweets;
	}
	
	public void setAvgLenOfTweets(long avgLenOfTweets)
	{
	
		this.avgLenOfTweets = avgLenOfTweets;
	}
}
